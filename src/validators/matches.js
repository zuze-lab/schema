import { SchemaType } from '../utils';
import { restrict } from './utils';

const test = (regexp, validateEmpty) => value =>
  (!validateEmpty && !value) ||
  (Array.isArray(regexp) ? regexp : [regexp]).some(
    exp => !!value.match(typeof exp === 'string' ? new RegExp(exp) : exp)
  );

const matches = (
  regexp,
  { validateEmpty = false, name = 'matches', message, params = {} } = {}
) => ({
  name,
  test: test(regexp, validateEmpty),
  params: { regexp, ...params },
  message,
  check: restrict([SchemaType.STRING], name),
});

